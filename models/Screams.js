class Screams {
  constructor(
    screamId,
    body,
    userHandle,
    createdAt,
    commentCount,
    likeCount,
    userImage
  ) {
    this.screamId = screamId;
    this.body = body;
    this.userHandle = userHandle;
    this.createdAt = createdAt;
    this.commentCount = commentCount;
    this.likeCount = likeCount;
    this.userImage = userImage;
  }
}
